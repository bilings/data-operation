public enum CoeGroupHasData {

	/**
	 * 有值
	 */
	YES(1,"有值"),
	/**
	 * 无值
	 */
	NO(2,"无值");
	
	private Integer code;
	private String value;
	private CoeGroupHasData(Integer code,String value) {
		this.code=code;
		this.value=value;
	}
	public Integer getCode() {
		return code;
	}
	public void setCode(Integer code) {
		this.code = code;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
}
