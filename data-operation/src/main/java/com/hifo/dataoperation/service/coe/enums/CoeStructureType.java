package com.hifo.dataoperation.service.coe.enums;
/**
 * 
 * @author 杨捷
 * @Date 2019年4月30日
 * @Description 系数结构体树节点类型
 */
public enum CoeStructureType {
	/**
	 * 根节点
	 */
	root(1,"根节点"),
	/**
	 * 下级叶子节点
	 */
	leaf(2,"下级叶子节点");
	
	private Integer code;
	private String value;

	private CoeStructureType(Integer code, String value) {
		this.code = code;
		this.value = value;
	}

	public Integer getCode() {
		return code;
	}

	public void setCode(Integer code) {
		this.code = code;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
}
