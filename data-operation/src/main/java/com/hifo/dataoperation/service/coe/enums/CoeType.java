package com.hifo.dataoperation.service.coe.enums;

/**
 * 
 * @author 杨捷
 * @Date 2019年4月29日
 * @Description 系数类型
 */
public enum CoeType {
	/**
	 * 楼层系数
	 */
	FLOOR(1,"楼层系数"),
	/**
	 * 朝向系数
	 */
	ORIENTATION(2,"朝向系数"),
	/**
	 * 建筑结构系数
	 */
	STRUCTURE(3,"建筑结构系数"),
	/**
	 * 面积系数
	 */
	AREA(4,"面积系数"),
	/**
	 * 景观系数
	 */
	SCENERY(5,"景观系数");

	private Integer code;
	private String value;

	private CoeType(Integer code, String value) {
		this.code = code;
		this.value = value;
	}

	public Integer getCode() {
		return code;
	}

	public void setCode(Integer code) {
		this.code = code;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
}
